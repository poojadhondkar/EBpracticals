package branchingStatement;

public class IfElsedemo {

	public static void main(String[] args) {
		
      int x;
      x=5;
      
      if(x<5)
      {
    	  x=x+10;
    	  System.out.println("this is if block"+x);
      }
      else {
    	  
    	  x=x+5;
    	  System.out.println("this is else block"+x);
      }
	}

}
